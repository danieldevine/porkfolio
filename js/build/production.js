var fx = {
  static: function(orientation, complete) {
    var $children = that.$container.children(),
        $target   = $children.eq(orientation.upcoming_static);

    $target.css({
      left: orientation.upcoming_position,
      display: 'block'
    });

    that.$control.animate({
      left: orientation.offset
    },
    that.options.animation_speed,
    that.options.animation_easing,
    function() {
      if (that.size() > 1) {
        that.$control.css({
          left: -that.width
        });

        $children.eq(orientation.upcoming_static).css({
          left: that.width,
          zIndex: 2
        });

        if (orientation.outgoing_static >= 0) {
          $children.eq(orientation.outgoing_static).css({
            left: that.width,
            display: 'none',
            zIndex: 0
          });
        }
      }

      complete();
    });
  },
  fade: function(orientation, complete) {
    var that = this,
        $children = that.$container.children(),
        $outgoing = $children.eq(orientation.outgoing_static),
        $target = $children.eq(orientation.upcoming_static);

    $target.css({
      left: this.width,
      opacity: 1,
      display: 'block'
    });

    if (orientation.outgoing_static >= 0) {
      $outgoing.animate({
        opacity: 0
      },
      that.options.animation_speed,
      that.options.animation_easing,
      function() {
        if (that.size() > 1) {
          $children.eq(orientation.upcoming_static).css({
            zIndex: 2
          });

          if (orientation.outgoing_static >= 0) {
            $children.eq(orientation.outgoing_static).css({
              opacity: 1,
              display: 'none',
              zIndex: 0
            });
          }
        }

        complete();
      });
    } else {
      $target.css({
        zIndex: 2
      });
      complete();
    }
  }
};

